// conditionals in JS

var burgerPrice = 150;
var isFriday = false;

if (burgerPrice <= 250 && isFriday == true) {
  console.log("I will eat the Burger and Chicken.");
} else if (burgerPrice <= 150 && isFriday == false) {
  console.log("I will eat just Burger.");
} else {
  console.log("I will eat just Chicken.");
}

