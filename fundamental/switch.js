// switch in JS

var car = "tesla";

switch (car) {
  case "bmw":
    console.log("bmw is great");
    break;
  case "ford":
    console.log("ford is good");
    break;
  case "tesla":
    console.log("tesla is my favorite");
    break;
  case "porsche":
    console.log("porsche is best");
    break;
  case "jaguar":
    console.log("jaguar is good");
    break;

  default:
    console.log("None brand car");
    break;
}

